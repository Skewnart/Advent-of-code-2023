        public string ExecutePart1()
        {
            var input = G.GetInput();
            long sum = 0;

            List<long> result = new List<long>();

            foreach (var board in input)
            {
                int vsym = board.GetVerticalSymmetry();
                int hsym = board.GetHorizontalSymmetry();
                int vsymsmudge = board.GetVerticalSymmetry(true);
                int hsymsmudge = board.GetHorizontalSymmetry(true);

                if (vsymsmudge != -1)
                {
                    sum += (vsymsmudge);
                }
                else if (hsymsmudge != -1)
                {
                    sum += 100 * (hsymsmudge);
                }
                else if (vsym != -1)
                {
                    result.Add(vsym);
                    sum += (vsym);
                }
                else if (hsym != -1)
                {
                    result.Add(100 * hsym);
                    sum += 100 * hsym;
                }
            }

            return sum.ToString();
        }
