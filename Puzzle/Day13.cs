        public static int GetHorizontalSymmetry(this string[] board, bool smudge = false)
        {
            bool needtosmudge = smudge;

            int height = board.Length;
            for (int i = 0; i < height - 1; i++)
            {
                var hori1 = board.GetHorizontal(i);
                var hori2 = board.GetHorizontal(i + 1);
                if (hori1.EqualsTo(hori2, ref smudge))
                {
                    bool seemstobesymmetric = true;
                    for (int j = i - 1; j >= 0 && 2 * i + 1 - j < height; j--)
                    {
                        var horij1 = board.GetHorizontal(j);
                        var horij2 = board.GetHorizontal(2 * i + 1 - j);
                        if (!horij1.EqualsTo(horij2, ref smudge))
                        {
                            seemstobesymmetric = false;
                            break;
                        }
                    }
                    if (seemstobesymmetric && (!needtosmudge || (needtosmudge && !smudge))) return i + 1;
                }
            }

            return -1;
        }
        public static int GetVerticalSymmetry(this string[] board, bool smudge = false)
        {
            bool needtosmudge = smudge;

            int width = board[0].Length;
            for (int i = 0; i < width - 1; i++)
            {
                var vert1 = board.GetVertical(i);
                var vert2 = board.GetVertical(i + 1);
                if (vert1.EqualsTo(vert2, ref smudge))
                {
                    bool seemstobesymmetric = true;
                    for (int j = i - 1; j >= 0 && i + 1 + i - j < width; j--)
                    {
                        var vertj1 = board.GetVertical(j);
                        var vertj2 = board.GetVertical(i + 1 + i - j);
                        if (!vertj1.EqualsTo(vertj2, ref smudge))
                        {
                            seemstobesymmetric = false;
                            break;
                        }
                    }
                    if (seemstobesymmetric && (!needtosmudge || (needtosmudge && !smudge))) return i + 1;
                }
            }

            return -1;
        }
        public static char[] GetVertical(this string[] board, int index)
        {
            List<char> list = new List<char>();
            foreach (string line in board) list.Add(line[index]);
            return list.ToArray();
        }
        public static char[] GetHorizontal(this string[] board, int index)
        {
            return board[index].ToArray();
        }
        public static bool EqualsTo(this char[] a, char[] b, ref bool smudge)
        {
            int nbdiff = 0;
            for (int i = 0; i < a.Length; i++)
            {
                if (a[i] != b[i])
                {
                    nbdiff++;
                }

            }

            if (nbdiff == 0) return true;
            if (smudge && nbdiff == 1)
            {
                smudge = false;
                return true;
            }

            return false;
        }
    }
